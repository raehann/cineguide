    private int getTotalRows() {
        try {
            Connection cn = k.getCon();
            Statement st = cn.createStatement();
            rs = st.executeQuery("SELECT COUNT(*) FROM film");
            if (rs.next()) {
                return rs.getInt(1);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        return 0;
    }
